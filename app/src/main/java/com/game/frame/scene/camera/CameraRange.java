public class CameraRange {
    public float xMin;
    public float yMin;
    public float xMax;
    public float yMax;

    public CameraRange(float pXMix, float pYMin, float pXMax, float pYMan) {
        xMin = pXMix;
        yMin = pYMin;
        xMax = pXMax;
        yMax = pYMan;
    }


}
